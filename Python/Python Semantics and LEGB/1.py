# 1. What is a variable in Python?
# ------------------------------------------------------------
# In Python, a variable is just a NAME.
# The name points to an OBJECT in memory.
# The value does not live inside the variable.
# Objects live independently in memory.
# Names only reference those objects.


# Example
x = 10          # x is bound to the object 10
y = x           # y points to the same object as x
